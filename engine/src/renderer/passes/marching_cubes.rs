    pub fn execute(
        &mut self,
        command_buffer: &mut CommandBuffer,
        pass_params: &RenderPassParameters<'_>,
        volume_texture: TextureHandle,
        threshold: f32,
        spacing: Vec3,
    ) {
        if self.executed_count > 0u32 {
            //return;
        }
        self.executed_count += 1u32;

        command_buffer.begin_label("Marching Cubes pass");

        let atomics_slice = pass_params.resources.access_buffer(
            command_buffer,
            Self::ATOMICS_BUFFER_NAME,
            BarrierSync::COMPUTE_SHADER,
            BarrierAccess::STORAGE_WRITE,
            HistoryResourceEntry::Current,
        );
        let vertices_slice = pass_params.resources.access_buffer(
            command_buffer,
            Self::VERTICES_BUFFER_NAME,
            BarrierSync::COMPUTE_SHADER,
            BarrierAccess::STORAGE_WRITE,
            HistoryResourceEntry::Current,
        );
        let indices_slice = pass_params.resources.access_buffer(
            command_buffer,
            Self::INDICES_BUFFER_NAME,
            BarrierSync::COMPUTE_SHADER,
            BarrierAccess::STORAGE_WRITE,
            HistoryResourceEntry::Current,
        );
        command_buffer.flush_barriers();
        command_buffer.clear_storage_buffer(
            BufferRef::Regular(&atomics_slice),
            0u64,
            pass_params
                .resources
                .buffer_range(Self::ATOMICS_BUFFER_NAME)
                .length
                / 4,
            0u32,
        );
        command_buffer.clear_storage_buffer(
            BufferRef::Regular(&vertices_slice),
            0u64,
            pass_params
                .resources
                .buffer_range(Self::VERTICES_BUFFER_NAME)
                .length
                / 4,
            0u32,
        );
        command_buffer.clear_storage_buffer(
            BufferRef::Regular(&indices_slice),
            0u64,
            pass_params
                .resources
                .buffer_range(Self::INDICES_BUFFER_NAME)
                .length
                / 4,
            0u32,
        );
        let atomics_slice = pass_params.resources.access_buffer(
            command_buffer,
            Self::ATOMICS_BUFFER_NAME,
            BarrierSync::COMPUTE_SHADER,
            BarrierAccess::STORAGE_WRITE | BarrierAccess::STORAGE_READ,
            HistoryResourceEntry::Current,
        );
        let vertices_slice = pass_params.resources.access_buffer(
            command_buffer,
            Self::VERTICES_BUFFER_NAME,
            BarrierSync::COMPUTE_SHADER,
            BarrierAccess::STORAGE_WRITE,
            HistoryResourceEntry::Current,
        );
        let indices_slice = pass_params.resources.access_buffer(
            command_buffer,
            Self::INDICES_BUFFER_NAME,
            BarrierSync::COMPUTE_SHADER,
            BarrierAccess::STORAGE_WRITE,
            HistoryResourceEntry::Current,
        );
        command_buffer.flush_barriers();

        let volume_texture = pass_params.assets.get_texture(volume_texture);

        let pipeline = pass_params
            .assets
            .get_compute_pipeline(self.pipeline)
            .unwrap();
        command_buffer.set_pipeline(PipelineBinding::Compute(&pipeline));
        command_buffer.bind_storage_buffer(
            BindingFrequency::Frequent,
            0,
            BufferRef::Regular(&self.edges_buffer),
            0,
            WHOLE_BUFFER,
        );
        command_buffer.bind_storage_buffer(
            BindingFrequency::Frequent,
            1,
            BufferRef::Regular(&self.tris_buffer),
            0,
            WHOLE_BUFFER,
        );
        command_buffer.bind_storage_texture(BindingFrequency::Frequent, 2, &volume_texture.view);
        command_buffer.bind_storage_buffer(
            BindingFrequency::Frequent,
            3,
            BufferRef::Regular(&vertices_slice),
            0,
            WHOLE_BUFFER,
        );
        command_buffer.bind_storage_buffer(
            BindingFrequency::Frequent,
            4,
            BufferRef::Regular(&indices_slice),
            0,
            WHOLE_BUFFER,
        );
        command_buffer.bind_storage_buffer(
            BindingFrequency::Frequent,
            5,
            BufferRef::Regular(&atomics_slice),
            0,
            WHOLE_BUFFER,
        );

        command_buffer.set_push_constant_data(
            &[MarchingCubesConfig {
                threshold,
                scale: spacing * 0.01f32,
            }],
            ShaderType::ComputeShader,
        );

        let texture_info = volume_texture.view.texture().unwrap().info();

        command_buffer.finish_binding();
        command_buffer.dispatch(
            (texture_info.width + 7u32) / 8u32,
            (texture_info.height + 7u32) / 8u32,
            (texture_info.depth + 7u32) / 8u32,
        );

        command_buffer.end_label();
    }

